
def fizzbuzz(int)
 if int % 3 == 0  && int % 5 == 0 # if the number int is divisible by 3 and 5. put first so that not return "Fizz" or "Buzz" as passes this condition first before moving to next
  "FizzBuzz" # Go fizz
 else if int % 5 == 0 # if the number int is divisible by 5
  "Buzz"
 else if int % 3 == 0 # if the number int is divisible by 3
   "Fizz"
 end
end
end
end